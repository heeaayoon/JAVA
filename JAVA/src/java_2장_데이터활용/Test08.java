package java_2장_데이터활용;

public class Test08 {

	public static void main(String[] args) {
		// 비교 연산자
		int a = 10;
		int b = 20;

		System.out.println(a < b);
		System.out.println(a > b);
		System.out.println(a <= b);
		System.out.println(a >= b);
		System.out.println(a == b);
		System.out.println(a != b);

	}
}
