package java_2장_데이터활용;

public class Test09 {

	public static void main(String[] args) {
		// 복합 대입 연산자
		int a = 10;
		int b = 2;

		a += b; // a = a + b
		System.out.println(a);

		a -= b; // a = a - b
		System.out.println(a);

		a *= b; // a = a * b
		System.out.println(a);

		a /= b; // a = a / b
		System.out.println(a);

		a %= b; // a = a % b
		System.out.println(a);

	}

}
